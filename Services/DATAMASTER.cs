             *  Then, return the key of the APPLICATION when it is found.
             */
            query.CommandText = "SELECT * FROM APPLICATION ORDER BY categoryID ASC, appID ASC;";
            read = query.ExecuteReader();

            while (read.Read())
            {
                if (read.GetString(appColumn) == app.Name && read.GetInt32(categoryColumn) == FindCategoryID(app.Category) && read.GetInt32(accountColumn) == app.User.ID)
