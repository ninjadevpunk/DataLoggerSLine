                            case "FILM":
                                return LOG.CATEGORY.FILM;
                            case "NOTES":
                                return LOG.CATEGORY.NOTES;
                        }
                    }
                }
            }

            read.Close();

            return LOG.CATEGORY.CODING;
        }


